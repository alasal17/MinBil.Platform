class LoadingPage extends React.Component {
  constructor(props) {
    super(props);
    this.targets = [];
  }

  componentDidMount() {
    let animeProps = Object.assign({ targets: this.targets, rotate:360, easing: "linear", duration: 5000,
    loop: true, translateX: 100,
    direction: 'alternate', ResizeObserverSize:414}, this.props);

    delete animeProps.children;

    this.anime = anime(animeProps);
  }

  addTarget = newTarget => {
    this.targets = [...this.targets, newTarget];
  };

  render() {
    let children = [];
    if (this.props.children) {
      if (Array.isArray(this.props.children)) children = this.props.children;
      else children = [this.props.children];
    }

    return (
      <g>
        {children.map((child, i) =>
          React.cloneElement(child, { key: i, ref: this.addTarget })
        )}
      </g>
    );
  }
}

export default LoadingPage;